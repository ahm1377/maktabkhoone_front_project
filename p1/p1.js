let slideIndex = 0;
let isMouseInside = false;
let timer;

showSlides();

function showSlides() {
  let i;
  let slides = document.getElementsByClassName("mySlides");
  let dots = document.getElementsByClassName("dot");
  
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none";  
  }
  
  slideIndex++;
  
  if (slideIndex > slides.length) {
    slideIndex = 1;
  }    
  
  for (i = 0; i < dots.length; i++) {
    dots[i].className = dots[i].className.replace(" active", "");
  }
  
  slides[slideIndex-1].style.display = "block";  
  dots[slideIndex-1].className += " active";
  
  // اگر موس وارد شده باشد، توقف تایمر
  if (!isMouseInside) {
    timer = setTimeout(showSlides, 2000); // Change image every 2 seconds
  }
}

// وقتی موس وارد می‌شود
function mouseEnter() {
  isMouseInside = true;
  clearTimeout(timer); // توقف تایمر
}

// وقتی موس از داخل خارج می‌شود
function mouseLeave() {
  isMouseInside = false;
  showSlides(); // ادامه تایمر
}

// اتصال توابع به رویدادهای موس
document.addEventListener("DOMContentLoaded", function () {
  document.querySelector(".mySlides-container").addEventListener("mouseenter", mouseEnter);
  document.querySelector(".mySlides-container").addEventListener("mouseleave", mouseLeave);
});

